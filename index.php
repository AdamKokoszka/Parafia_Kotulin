                <p>
                  Początek osady wiąże się z Kotulinem Małym, który posiadał swój kościół. Pierwsze udokumentowane wzmianki o osadzie na tym terenie pochodzą z roku 1257. Mianowicie hrabia Januzjusz 20 stycznia 1257r. uzyskał zezwolenie od ks. Władysława na założenie według prawa niemieckiego nowego osiedla w lesie Chotulin. Osiedle to, później wieś, od 1305r. w dokumentach nosi nazwę Magnum Chotulin (Kotulin Wielki), w odróżnieniu od Kotulina Małego, datowanego o wiele wcześniej. Według źródeł w Kotulinie Wielkim już przed 1305r. została erygowana parafia. W 1285r. z nieznanego miejsca przeniesiono do Kotulina Wielkiego kościół pw. św. Michała Archanioła i ustawiono go pośrodku cmentarza. Pierwszym proboszczem został ks. Aleksius. Aż do roku 1502 parafia miała dwa kościoły - w Kotulinie Małym i Wielkim. Do parafii w Kotulinie dołączono także wieś Proboszczowice (założona około roku 1305). Według niektórych źródeł dopiero od roku 1376 kościół w Kotulinie Wielkim jest zwany parafialnym. W 1412r. Parafia kotulińska należała do hr.Jenkiego, a w 1495r. obejmuje ją Hrabia Mokrzęski. W 1562r. właścicielem parafii został hr. Starzinsky. Kotulin przechodzi na protestantyzm. Trwa to jednak niedługo, bo tylko do roku 1629r. W 1648r. Kotulin zostaje kupiony przez właścicieli Toszka. Od 1890 roku aż do zakończenia II wojny światowej, właścicielami Kotulina jest rodzina Hohenlohe ze Sławięcic. Z Kotulina wywodzi się znana rodzina szlachecka Kottulinskich.</p>
